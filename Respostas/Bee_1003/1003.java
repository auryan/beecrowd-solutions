import java.io.IOException;
import java.util.Scanner;

public class Main {
	public static void main(String[] args) throws IOException {
		int A, B, SOMA;
		
		Scanner input = new Scanner(System.in);
		
		A = input.nextInt();
		B = input.nextInt();
		
		SOMA = A + B;
		
		System.out.printf("SOMA = %d\n", SOMA);
	}
}